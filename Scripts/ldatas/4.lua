function checkhitaoe(pos1, pos2, radius, maxDist, target, playerradius)
	if playerradius == nil then
		playerradius = target
		target = maxDist
		maxDist = 0
	end
	local distancePos1Pos2 = GetDistance(pos1, pos2)
	local pos12 = (Vector(pos2)-pos1):normalized()
	local pos3 = (maxDist > 0 and maxDist < distancePos1Pos2) and (pos1 + pos12*maxDist) or pos2
    local distancePos3 = GetDistance(target, pos3)
	return (distancePos3 < radius+playerradius)
end
function checkhitlinepoint(pos1, pos2, radius, maxDist, target, playerradius)
	if playerradius == nil then
		playerradius = target
		target = maxDist
		maxDist = 0
	end
	local distancePos1Pos2 = GetDistance(pos1, pos2)
	local pos12 = (Vector(pos2)-pos1):normalized()
	local pos3 = (maxDist > 0 and maxDist < distancePos1Pos2) and (pos1 + pos12*maxDist) or pos2
	local closestPoint = VectorPointProjectionOnLine(pos1, pos3, target)
	local distanceCP = GetDistance(closestPoint, target)
	local distanceCPPos1 = GetDistance(closestPoint, pos1)
	local distanceCPPos3 = GetDistance(closestPoint, pos3)
	local distancePos1Pos3 = GetDistance(pos1, pos3)
	return (distanceCP < radius+playerradius and distanceCPPos1 < distancePos1Pos3 and distanceCPPos3 < distancePos1Pos3)
end
function checkhitlinepass(pos1, pos2, radius, maxDist, target, playerradius)
	local pos12 = (Vector(pos2)-pos1):normalized()
	local pos3 = pos1 + pos12*maxDist
	local closestPoint = VectorPointProjectionOnLine(pos1, pos3, target)
	local distanceCP = GetDistance(closestPoint, target)
	local distanceCPPos1 = GetDistance(closestPoint, pos1)
	local distanceCPPos3 = GetDistance(closestPoint, pos3)
	local distancePos1Pos3 = GetDistance(pos1, pos3)
	return (distanceCP < radius+playerradius and distanceCPPos1 < distancePos1Pos3 and distanceCPPos3 < distancePos1Pos3)
end
function checkhitcone(pos1, pos2, angle, maxDist, target, playerradius)
	local pos12 = (Vector(pos2)-pos1):normalized()
	local pos3 = pos1 + pos12*maxDist
    local closestPoint = VectorPointProjectionOnLine(pos1, pos3, target)
	local distancePos1 = GetDistance(target, pos1)
	local radius = (math.tan(math.rad(angle/2)))*distancePos1
	local distanceCP = GetDistance(closestPoint, target)	
	local distanceCPPos1 = GetDistance(closestPoint, pos1)
	local distanceCPPos3 = GetDistance(closestPoint, pos3)
	local distancePos1Pos3 = GetDistance(pos1, pos3)
	return (distanceCP < radius+playerradius and distanceCPPos1 < distancePos1Pos3 and distanceCPPos3 < distancePos1Pos3)
end
function checkhitwall(pos1, pos2, radius, maxradius, target, playerradius)
	local pos3 = ((Vector(pos2)-pos1):perpendicular()):normalized()*maxradius
	local pos4 = Vector(pos2)-pos3
	local pos5 = Vector(pos2)+pos3
	local closestPoint = VectorPointProjectionOnLine(pos4, pos5, target)
	local distanceCP = GetDistance(closestPoint, target)
	local distanceCPPos4 = GetDistance(closestPoint, pos4)
	local distanceCPPos5 = GetDistance(closestPoint, pos5)
	local distancePos4Pos5 = GetDistance(pos4, pos5)
	return (distanceCP < radius+playerradius and distanceCPPos4 < distancePos4Pos5 and distanceCPPos5 < distancePos4Pos5)
end

