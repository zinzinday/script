local myTeamWealth = {}
local enemyTeamWealth = {}


local xc, yc = 1675, 50

local function PrintLine(str, line, col)
   Text(str,xc+col*5,yc+line*15,0xFFCCEECC);
end

function sortWealth(a,b)
   if a.wealth == b.wealth then
      return a.name > b.name
   end
   return a.wealth > b.wealth
end

function DrawHeroWealth()
	local myTeam, enemyTeam = 0, 0

	for i=1, objManager:GetMaxHeroes() do
		local hero = objManager:GetHero(i)
		local teamWealth
		if hero.team == me.team then
			teamWealth = myTeamWealth
		else
			teamWealth = enemyTeamWealth
		end

		local wealth = 0
		for slot=1, 7 do
			local item = itemPrices[hero["InventorySlot"..slot]]			
			if item then
				wealth = wealth + item.price
			end
		end	
		local foundHero = false
		for i,cw in ipairs(teamWealth) do
			if cw.name == hero.name then
				cw.wealth = wealth
				foundHero = true
			end
		end
		if not foundHero then
			table.insert(teamWealth, {name=hero.name,wealth=wealth})
		end
	end
	
	table.sort(myTeamWealth, sortWealth)
	table.sort(enemyTeamWealth, sortWealth)

	local total = 0
	for i, cw in ipairs(myTeamWealth) do
		PrintLine(cw.name, 2+i, 0)
		PrintLine(cw.wealth, 2+i, 15)
		total = total + cw.wealth
	end
	PrintLine(total, 2, 8)

	local total = 0
	for i, cw in ipairs(enemyTeamWealth) do
		PrintLine(cw.name, 2+i, 25)
		PrintLine(cw.wealth, 2+i, 40)
		total = total + cw.wealth
	end
	PrintLine(total, 2, 33)
end	
