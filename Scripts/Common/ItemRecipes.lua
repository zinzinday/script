function RecipeOfItem(itemID)
	return ItemTable[itemID] ~= nil and ItemTable[itemID].recipe or {}
end

function NameOfItem(itemID)
	return ItemTable[itemID].name
end

function IDofItem(itemName)
	for item, data in pairs(ItemTable) do
		if string.lower(data.name) == string.lower(itemName) then
			return item
		end
	end
end

function ReturnItemTable()
	return ItemTable
